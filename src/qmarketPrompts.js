export function systemPromptFor(version) {
  return version === 'blender3d' ? BLENDER3D : REALISTIC
}

export function buildQmarketMessages({ version, brief }) {
  const b = brief || {}
  const lines = []
  if (b.title) lines.push(`타이틀: ${b.title}`)
  if (b.subtitle) lines.push(`서브 문구: ${b.subtitle}`)
  if (b.concept) lines.push(`컨셉 / 상품 구성: ${b.concept}`)
  const briefText = lines.join('\n') || '(브리프 없음 — 일반적인 큐마켓 상세페이지 메인 키이미지)'

  const user = `아래 브리프로, 규칙을 지키는 큐마켓 상세페이지 메인 키이미지용 이미지 생성 프롬프트 1개를 한국어로 작성해줘.

${briefText}

다시 강조: 타이틀과 서브 문구는 분위기 참고와 상단 여백 확보용일 뿐, 그 글자 자체를 이미지에 넣지 말 것. 프롬프트 본문만 출력.`

  return [
    { role: 'system', content: systemPromptFor(version) },
    { role: 'user', content: user },
  ]
}
